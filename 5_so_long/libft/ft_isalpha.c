int	ft_isalpha(int c)
{
	int	resp;

	resp = 1024;
	if (c < 'A')
	{
		resp = 0;
	}
	else if (c > 'Z' && c < 'a')
	{
		resp = 0;
	}
	else if (c > 'z')
	{
		resp = 0;
	}
	return (resp);
}
